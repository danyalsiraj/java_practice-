
public class s_w_i {

	public static void main(String[] args) {
		// statement is the entire line
		int myVar = 40; // myVar=40 is an expression and int myVar=40; is a
						// statement

		myVar++;
		myVar--;
		System.out.println("this is a test");

		System.out.println("this is" + "another" + "test");
		// we can have multiple statements in one line
		int secondVar = 50;
		secondVar--;
		System.out.println("testing");

		// whitespace: space in between your expressions and variables. spaces
		// dont matter as much
		// indentation: makes the code easier to follow, doesnt effect the
		// functionality, ctrl+shift+f for auto formatting
		
	}
}
